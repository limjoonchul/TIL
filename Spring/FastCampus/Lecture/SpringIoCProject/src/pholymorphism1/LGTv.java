package pholymorphism1;

public class LGTv {

	 public LGTv() {
	        System.out.println("===> LGTv 생성");
	    }

	    public void turnOn(){
	        System.out.println("LGTv---전원 켠다.");
	    }

	    public void turnOff(){
	        System.out.println("LGTv---전원 끈다.");
	    }

	    public void soundUp(){
	        System.out.println("LGTv---소리 올린다.");
	    }

	    public void soundDown(){
	        System.out.println("LGTv---소리 내린다.");
	    }
}
